<template>
  <div class="card">
    <h3>
      {{ product.name }}
    </h3>
    <h5 class="price">Price: ${{ product.price.toFixed(2) }}</h5>
    <p class="description">Description: ${{ description }}</p>
  </div>
</template>

<script>
export default {
  props: ["product"],
  computed: {
    description() {
      return this.product.content.substring(0, 100) + "...";
    },
  },
};
</script>
